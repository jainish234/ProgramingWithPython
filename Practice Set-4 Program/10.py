# 10) Accept string from the user; convert the string to upper case.

# Accept input string from the user
input_string = input("Enter a string: ")

# Convert the string to uppercase
upper_case_string = input_string.upper()

# Display the uppercase string
print("Uppercase string:", upper_case_string)

