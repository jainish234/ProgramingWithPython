#17) Take two integer values from the user using split(), perform basic arithmetic operation on the values.
n1,n2=[int(no)for no in input('enter 2 integer value by giving space:').split()]
print('the sum of 2 integer value is :',n1+n2)
print('the substract of 2 integer value is :',n1-n2)
print('the division of 2 integer value is :',n1/n2)
print('the multiplication of 2 integer value is :',n1*n2)
print('the module of 2 integer value is :',n1%n2)

